export type TIconDefinition =
    [string, {}] |
    [string, TIconDefinitionArray] |
    [string, {}, TIconDefinitionArray] |
    [string, TIconDefinitionArray, {}];

interface TIconDefinitionArray extends Array<TIconDefinition> { }

export interface IIconsDefinition {
    [name: string]: TIconDefinition
}



/**
 * Register new icons.
 * @param {object}  icons - The  keys are the  name of the  icons, the
 * value is the object representing an SVG image.
 * @returns {undefined}
 */
